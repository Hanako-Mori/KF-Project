export async function getStaticProps() {
  // ðŸ‚Cheerio boiler plate:ðŸƒ
  // ðŸŒ¿Get html data -> convernt to string -> convert to Cheerio objectðŸŒ¿
  const index = "https://japari-library.com";
  const hub = "/wiki/List_of_Friends";
  const hubHtml = await fetch(index + hub);
  const hubString = await hubHtml.text();
  const $ = cheerio.load(hubString);

  // ðŸ‚Declare listsðŸƒ
  let friendNames: String[] = [];
  let friendPages: String[] = [];
  let friendImages: String[] = [];

  // ðŸ‚Fill friendNames and friendLinksðŸƒ
  $("img")
    .parent()
    .each(function (i, element) {
      let name = $(element).attr("title");
      let page = $(element).attr("href");
      if (page != undefined) {
        friendPages[i] = page;
      }
      if (name != undefined) {
        friendNames[i] = name;
      }
    });

  // ðŸ‚Print friendNames and friendLinksðŸƒ
  console.log(friendNames);
  console.log(friendPages);

  // ðŸ‚Fill friendImagesðŸƒ
  // ðŸŒ¿Cheerio boilerplate:ðŸŒ¿
  // let i = 40;
  // const pageHtml = await fetch(index + friendPages[i]);

  return {
    props: { friendNames, friendPages },
  };
}

